export function getHexForName(colorName: string) {
	for (const hexCode in colorsByHex) {
		const colorNames = colorsByHex[hexCode].map((name) => name.toLowerCase());
		if (colorNames.includes(colorName.toLowerCase())) return hexCode;
	}
	return null;
}

export function getNamesForHex(hexCode: string) {
	hexCode = hexCode.toUpperCase();
	if (hexCode in colorsByHex) return colorsByHex[hexCode];
	return [];
}
